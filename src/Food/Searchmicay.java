    private void muaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_muaActionPerformed
        String quantityStr = JOptionPane.showInputDialog(this, "Nhập số lượng bạn muốn mua:");
    
    if (quantityStr != null && !quantityStr.trim().isEmpty()) {
        try {
            int quantity = Integer.parseInt(quantityStr);
            // Xử lý mua hàng thành công
            JOptionPane.showMessageDialog(this, "Mua thành công: " + quantity + " món " + jTextArea3.getText().split("\n")[0], "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            
            // Ghi vào file
            try (FileWriter writer = new FileWriter("donhang.txt", true)) {
                writer.write("Món: " + jTextArea3.getText().split("\n")[0] + "\n");
                writer.write("Số lượng: " + quantity + "\n");
                writer.write("-----\n");
            } catch (IOException e) {
                e.printStackTrace();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng không hợp lệ!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Bạn phải nhập số lượng!", "Thông báo", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_muaActionPerformed

