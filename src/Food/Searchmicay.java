    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        String searchQuery = JOptionPane.showInputDialog(this, "Nhập từ khóa tìm kiếm:");
    
    try {
        if ("bún đậu mắm tôm".equalsIgnoreCase(searchQuery)) {
            new Searchbundaumamtom().setVisible(true);
            saveToFile("bún đậu mắm tôm");
        } else if ("mì cay".equalsIgnoreCase(searchQuery)) {
            new Searchmicay().setVisible(true);
            saveToFile("mì cay");
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy món ăn!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi khi lưu vào file!", "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
}

