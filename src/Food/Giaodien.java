private boolean isAddressClose(String userAddress, String foodAddress) {
    // Lấy số nhà từ địa chỉ của người dùng
    int userNumber = Integer.parseInt(userAddress.split(" ")[0].trim());
    // Lấy số nhà từ địa chỉ món ăn
    int foodNumber = Integer.parseInt(foodAddress.split(" ")[0].trim());

    // Kiểm tra khoảng cách
    return Math.abs(userNumber - foodNumber) <= 20; // Kiểm tra khoảng cách 20 số
}
    private void btngoiyganbanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngoiyganbanActionPerformed
        String address = javax.swing.JOptionPane.showInputDialog(this, "Nhập địa chỉ của bạn:");

    if (address != null && !address.trim().isEmpty()) {
        String[] foodAddresses = {
            jTextArea3.getText().split("\n")[1].trim(),
            jTextArea4.getText().split("\n")[1].trim(),
            jTextArea5.getText().split("\n")[1].trim()
        };

        StringBuilder recommendations = new StringBuilder("Món ăn gần bạn:\n");
        boolean found = false;

        for (String foodAddress : foodAddresses) {
            if (isAddressClose(address, foodAddress)) {
                recommendations.append(foodAddress).append("\n");
                found = true;
            }
        }

        if (found) {
            javax.swing.JOptionPane.showMessageDialog(this, recommendations.toString(), "Gợi ý món ăn", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Không có món ăn nào gần địa chỉ bạn nhập.", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Bạn phải nhập địa chỉ!", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
    }
    
    }//GEN-LAST:event_btngoiyganbanActionPerformed

