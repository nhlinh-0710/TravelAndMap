    private void ratingbungioheoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ratingbungioheoActionPerformed
        String[] comments = {
        "Bún giò heo rất ngon! ⭐⭐⭐⭐⭐",
        "Món ăn này thật tuyệt vời! ⭐⭐⭐⭐",
        "Chất lượng bún giò heo rất tốt! ⭐⭐⭐⭐⭐",
        "Giá cả hợp lý, rất đáng thử! ⭐⭐⭐⭐",
        "Tôi rất thích món này! ⭐⭐⭐⭐⭐",
        "Có thể cải thiện thêm một chút về gia vị. ⭐⭐⭐"
    };

    StringBuilder commentList = new StringBuilder("Đánh giá về bún giò heo:\n\n");
    List<String> likedComments = new ArrayList<>();
    List<String> dislikedComments = new ArrayList<>();

    // Tạo một JPanel để chứa bình luận và nút
    JPanel panel = new JPanel();
    panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));

    for (String comment : comments) {
        JCheckBox likeBox = new JCheckBox(comment); // Tạo checkbox cho mỗi bình luận
        panel.add(likeBox);
    }

    // Hiển thị hộp thoại
    int result = JOptionPane.showConfirmDialog(this, panel, "Đánh giá về bún giò heo", JOptionPane.OK_CANCEL_OPTION);
    
    if (result == JOptionPane.OK_OPTION) {
        for (Component comp : panel.getComponents()) {
            if (comp instanceof JCheckBox) {
                JCheckBox checkBox = (JCheckBox) comp;
                if (checkBox.isSelected()) {
                    likedComments.add(checkBox.getText());
                } else {
                    dislikedComments.add(checkBox.getText());
                }
            }
        }
        
        // Ghi vào file
        saveCommentsToFile(likedComments, dislikedComments);
    }
}

private void saveCommentsToFile(List<String> likedComments, List<String> dislikedComments) {
    try (FileWriter writer = new FileWriter("comments.txt", true)) {
        writer.write("Bình luận yêu thích:\n");
        for (String comment : likedComments) {
            writer.write(comment + "\n");
        }
        
        writer.write("\nBình luận không thích:\n");
        for (String comment : dislikedComments) {
            writer.write(comment + "\n");
        }
        
        writer.write("-----\n");
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi khi lưu vào file!", "Thông báo", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ratingbungioheoActionPerformed

