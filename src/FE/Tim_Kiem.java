    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
      // Lấy dữ liệu từ jTextField1
    String query = jTextField1.getText().toLowerCase();
    
    // Tạo JTextArea để hiển thị thông tin kết quả
    jTextArea1.setText(""); // Xóa nội dung cũ
    
    try {
        // Điều kiện tìm kiếm
        if (query.contains("mộc châu")) {
            jTextArea1.setText("Kết quả: Mộc Châu\nClick để xem chi tiết!");
            openWebpage("https://vnexpress.net/cam-nang-du-lich-moc-chau-4098678.html");
        } else if (query.contains("phong nha")) {
            jTextArea1.setText("Kết quả: Phong Nha\nClick để xem chi tiết!");
            openWebpage("https://oxalisadventure.com/vi/7-diem-du-lich-phong-nha-thich-hop-gia-dinh-co-tre-nho/");
        } else if (query.contains("đà lạt")) {
            jTextArea1.setText("Kết quả: Đà Lạt\nClick để xem chi tiết!");
            openWebpage("https://vnexpress.net/cam-nang-du-lich-da-lat-4455699.html");
        } else {
            jTextArea1.setText("Không tìm thấy kết quả phù hợp!");
        }
    } catch (Exception e) {
        e.printStackTrace();
        jTextArea1.setText("Đã xảy ra lỗi trong quá trình tìm kiếm!");
    }


    }//GEN-LAST:event_jButton7ActionPerformed

