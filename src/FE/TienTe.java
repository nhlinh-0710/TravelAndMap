    public void loadConversionsToList() {
    listModel.clear(); // Xóa dữ liệu cũ trước khi tải dữ liệu mới

    try (Connection conn = MySQLConnection.getConnection()) {
        String sql = "SELECT amount, from_currency, to_currency, result FROM conversions ORDER BY converted_at DESC LIMIT 10";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            double amount = rs.getDouble("amount");
            String fromCurrency = rs.getString("from_currency");
            String toCurrency = rs.getString("to_currency");
            double result = rs.getDouble("result");

            // Thêm kết quả vào JList
            String display = String.format("%.2f %s → %.2f %s", amount, fromCurrency, result, toCurrency);
            listModel.addElement(display);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}





