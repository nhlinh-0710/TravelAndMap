    private void convertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertButtonActionPerformed

     try {
    double amount = Double.parseDouble(amountField.getText().trim());
    String from = (String) fromCurrency.getSelectedItem();
    String to = (String) toCurrency.getSelectedItem();

    if (from.equals(to)) {
        resultLabel.setText("Please choose two different currencies.");
        return;
    }

    // Convert to USD first
    double usdAmount;
    switch (from) {
        case "USD": usdAmount = amount; break;
        case "VND": usdAmount = amount / 25000; break;
        case "EUR": usdAmount = amount / 0.85; break;
        case "JPY": usdAmount = amount / 110.0; break;
        default:
            resultLabel.setText("Unsupported currency type: " + from);
            return;
    }

    // Convert from USD to the target currency
    double result;
    switch (to) {
        case "USD": result = usdAmount; break;
        case "VND": result = usdAmount * 25000; break;
        case "EUR": result = usdAmount * 0.85; break;
        case "JPY": result = usdAmount * 110.0; break;
        default:
            resultLabel.setText("Unsupported currency type: " + to);
            return;
    }

    // Display the result
    resultLabel.setText(String.format("Result: %.2f %s = %.2f %s", amount, from, result, to));

    // Save to MySQL
    String sql = "INSERT INTO conversions (amount, from_currency, to_currency, result) VALUES (?, ?, ?, ?)";

    try (Connection conn = MySQLConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setDouble(1, amount);
        ps.setString(2, from);
        ps.setString(3, to);
        ps.setDouble(4, result);

        int rowsInserted = ps.executeUpdate();
        if (rowsInserted > 0) {
            System.out.println("✅ Successfully saved to MySQL.");
        } else {
            System.out.println("⚠️ Failed to save to MySQL.");
        }

    } catch (SQLException e) {
        System.err.println("❌ Error connecting to or saving to MySQL:");
        e.printStackTrace();
        resultLabel.setText("Error saving result to database.");
    }

    // Update the JList
    loadConversionsToList();

} catch (NumberFormatException ex) {
    resultLabel.setText("❌ Please enter a valid number!");
}



    }//GEN-LAST:event_convertButtonActionPerformed

