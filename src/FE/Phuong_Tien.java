private String getDaLatTransportInfo() {
    return """
    🚀 **Vé máy bay**: Giá vé từ TP.HCM/Hà Nội đi Đà Lạt dao động từ **1.200.000 - 2.500.000 VNĐ**. Thời gian bay khoảng **1 giờ**.
    🚗 **Taxi**: Giá taxi từ sân bay Liên Khương về trung tâm Đà Lạt khoảng **250.000 - 350.000 VNĐ** (30 phút).
    🚆 **Tàu lửa**: Hiện tại không có tuyến tàu lửa trực tiếp đến Đà Lạt.
    🚌 **Xe bus**: Xe khách từ TP.HCM đi Đà Lạt có giá **250.000 - 400.000 VNĐ**, thời gian di chuyển **6-8 giờ**.

    ✅ **Ưu điểm**:
    - Máy bay nhanh, tiện lợi.
    - Xe khách giá rẻ, nhiều chuyến trong ngày.
    - Taxi phù hợp cho nhóm nhỏ, tiện lợi từ sân bay.

    ❌ **Nhược điểm**:
    - Máy bay giá cao vào mùa cao điểm.
    - Xe khách mất nhiều thời gian hơn.
    - Taxi đắt hơn so với xe bus.
    """;
}
private String getMocChauTransportInfo() {
    return """
    🚀 **Vé máy bay**: Không có chuyến bay trực tiếp, cần bay đến Hà Nội rồi đi xe khách. Giá vé từ **1.500.000 - 2.800.000 VNĐ**.
    🚗 **Taxi**: Giá taxi từ Hà Nội đi Mộc Châu khoảng **1.500.000 - 2.000.000 VNĐ** (4-5 giờ).
    🚆 **Tàu lửa**: Không có tuyến tàu lửa trực tiếp đến Mộc Châu.
    🚌 **Xe bus**: Xe khách từ Hà Nội đi Mộc Châu có giá **160.000 - 250.000 VNĐ**, thời gian di chuyển **4-5 giờ**.

    ✅ **Ưu điểm**:
    - Xe khách giá rẻ, nhiều chuyến trong ngày.
    - Taxi tiện lợi, không cần chờ đợi.
    - Máy bay giúp tiết kiệm thời gian nếu đi từ xa.

    ❌ **Nhược điểm**:
    - Không có tàu lửa hoặc máy bay trực tiếp.
    - Taxi giá cao hơn xe khách.
    """;
}
private String getSapaTransportInfo() {
    return """
    🚀 **Vé máy bay**: Không có chuyến bay trực tiếp, cần bay đến Hà Nội rồi đi tàu hoặc xe khách. Giá vé từ **1.500.000 - 3.000.000 VNĐ**.
    🚗 **Taxi**: Giá taxi từ Hà Nội đi Sa Pa khoảng **2.000.000 - 3.000.000 VNĐ** (5-6 giờ).
    🚆 **Tàu lửa**: Tàu từ Hà Nội đi Lào Cai có giá **250.000 - 800.000 VNĐ**, thời gian di chuyển **8-9 giờ**.
    🚌 **Xe bus**: Xe khách từ Hà Nội đi Sa Pa có giá **250.000 - 400.000 VNĐ**, thời gian di chuyển **5-6 giờ**.

    ✅ **Ưu điểm**:
    - Tàu lửa thoải mái, có giường nằm.
    - Xe khách nhanh hơn tàu, giá hợp lý.
    - Taxi tiện lợi, không cần chờ đợi.

    ❌ **Nhược điểm**:
    - Máy bay không có tuyến trực tiếp.
    - Taxi giá cao hơn xe khách.
    - Tàu lửa mất nhiều thời gian hơn.
    """;
}
    private String getTransportInfo(String location) {
    if (location.contains("đà lạt")) {
        return getDaLatTransportInfo();
    } else if (location.contains("mộc châu")) {
        return getMocChauTransportInfo();
    } else if (location.contains("sapa")) {
        return getSapaTransportInfo();
    } else {
        return "Hiện tại chỉ hỗ trợ thông tin cho Đà Lạt, Mộc Châu và Sa Pa.";
    }
}

