    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        
    String selectedCity = (String) jComboBox2.getSelectedItem();
    
    if (selectedCity.equals("Đà Nẵng")) {
        // Mở giao diện Đà Nẵng
        AddressnguoithichbanDaNang addressFrame = new AddressnguoithichbanDaNang();
        addressFrame.setVisible(true);
        this.dispose(); // Đóng cửa sổ hiện tại
    } else if (selectedCity.equals("Hồ Chí Minh")) {
        // Mở giao diện Hồ Chí Minh
        AddressnguoithichbanHoChiMinh addressFrame = new AddressnguoithichbanHoChiMinh();
        addressFrame.setVisible(true);
        this.dispose(); // Đóng cửa sổ hiện tại
    } else if (selectedCity.equals("Khác")) {
        // Mở giao diện Khác
        AddressNguoithichbanKHAC addressFrame = new AddressNguoithichbanKHAC();
        addressFrame.setVisible(true);
        this.dispose(); // Đóng cửa sổ hiện tại
    }
    }//GEN-LAST:event_jComboBox2ActionPerformed

