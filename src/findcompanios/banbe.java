    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        JDialog dialog = new JDialog();
    dialog.setTitle("Chat");
    dialog.setSize(300, 200);
    dialog.setLayout(null);

    JTextArea textArea = new JTextArea();
    textArea.setBounds(10, 10, 260, 100);
    dialog.add(textArea);

    JButton sendButton = new JButton("Gửi");
    sendButton.setBounds(10, 120, 80, 25);
    sendButton.addActionListener(new ActionListener() {
        public void actionPerformed(ActionEvent e) {
            String message = textArea.getText();
            try (BufferedWriter writer = new BufferedWriter(new FileWriter("messages.txt", true))) {
                writer.write(message);
                writer.newLine();
                JOptionPane.showMessageDialog(null, "Tin nhắn đã được gửi!");
            } catch (IOException ex) {
                ex.printStackTrace();
            }
            dialog.dispose();
        }
    });

    JButton cancelButton = new JButton("Hủy");
    cancelButton.setBounds(100, 120, 80, 25);
    cancelButton.addActionListener(new ActionListener() {
        public void actionPerformed(ActionEvent e) {
            dialog.dispose();
        }
    });

    dialog.add(sendButton);
    dialog.add(cancelButton);
    dialog.setVisible(true);
    }//GEN-LAST:event_jButton6ActionPerformed

